# List operations
def get_first(numbers: list[int]) -> int:
    print("Getting first element from list")
    first: int = numbers[0]
    print("First element is", first)
    return first

def get_third(numbers: list[int]) -> int:
    print("Getting third element from list")
    third: int = numbers[2]
    print("Third element is", third)
    return third
