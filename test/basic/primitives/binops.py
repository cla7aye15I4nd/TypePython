def test_bitand(a: int, b: int) -> int:
    return a & b

def test_bitor(a: int, b: int) -> int:
    return a | b

def test_bitxor(a: int, b: int) -> int:
    return a ^ b

def test_lshift(a: int, b: int) -> int:
    return a << b

def test_rshift(a: int, b: int) -> int:
    return a >> b

def test_pow(a: int, b: int) -> int:
    return a ** b
